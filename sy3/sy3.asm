DATAS SEGMENT
MES DB 'The result z is:$'
XL DW 65A1H
XH DW 0015H
YL DW 0B79DH
YH DW 0021H
DATAS ENDS

STACKS SEGMENT
STACKS ENDS

CODES SEGMENT
	ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
	MOV AX,DATAS
 	MOV DS,AX
    MOV DX,OFFSET MES
	MOV AH,09H
	INT 21H
	CALL COMPUTE 
	PUSH BX
 	CALL LIST_BX 
	POP BX
	MOV AX,BX
 	CALL LIST_BX 
	MOV AX,4C00H
	INT 21H
	
COMPUTE PROC
 	MOV AX,XL
	ADD AX,YL 
	MOV BX,AX 
	MOV AX,XH
 	ADC AX,YH 
	RET
COMPUTE ENDP

LIST_BX PROC 
 	MOV CH,4
 	MOV CL,4
 	MOV BX,AX
NEXT:
	ROL BX,CL
 	MOV DL,BL
 	AND DL,0FH
 	ADD DL,30H
 	CMP DL,39H
 	JLE PRINT
 	ADD DL,7
PRINT: 
	MOV AH,2H 
 	INT 21H
 	DEC CH
 	JNZ NEXT
 	RET
LIST_BX ENDP
CODES ENDS
    END START


